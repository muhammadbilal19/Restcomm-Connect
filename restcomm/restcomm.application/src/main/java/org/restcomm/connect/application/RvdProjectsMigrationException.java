package org.restcomm.connect.application;

/**
 * @author guilherme.jansen@telestax.com
 */
public class RvdProjectsMigrationException extends Exception {

    private String message;
    private int errorCode;

    public RvdProjectsMigrationException(String message, int errorCode) {
        super();
        this.message = message;
        this.errorCode = errorCode;
    }

    public RvdProjectsMigrationException(String message) {
        super();
        this.message = message;
    }

    public RvdProjectsMigrationException() {

    }

    @Override
    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

}
